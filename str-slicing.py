# Strings Indexing and slicing

food = "Amala is almost ready"

print(food[-3])

print(food[0:3])
print(food[9:-6])
print(food[9:15])
# I was able to slice amala from the sentence and this is the output

statement = f"I was able to slice {food[0:5]} from the sentence and this is the output"

print(statement)